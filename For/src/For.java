public class For {
    public static void main(String[] args) {

        // M&M
        for (int mem = 0; mem < 20; mem++) {
            System.out.println(mem);
        }
        /**
         *[Initialization: int mem = 0] = Starts counting from the number 0
         *[Continuation condition: mem < 20] = Continues the loop as long as "mem" is less than 20
         *[Update: mem++] = After each loop iteration, the value of "mem" is incremented by 1
         */

    }
}
